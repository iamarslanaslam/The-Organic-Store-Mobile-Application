package com.example.theorganicstoremobileapp.models;

import java.util.Date;

public class Employee {

    private String id; // Unique identifier for the employee


    private String firstName; // Employee's first name


    private String lastName; // Employee's last name


    private String email; // Employee's email address


    private String phone; // Employee's phone number


    private String position; // Employee's job title

    private double salary; // Employee's salary

    private Date hireDate; // Date the employee was hired

    private String department; // Department the employee belongs to

    public Employee() {}
    // Constructor
    public Employee(String id, String firstName, String lastName, String email, String phone,
                    String position, double salary, Date hireDate, String department) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phone = phone;
        this.position = position;
        this.salary = salary;
        this.hireDate = hireDate;
        this.department = department;
    }

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public Date getHireDate() {
        return hireDate;
    }

    public void setHireDate(Date hireDate) {
        this.hireDate = hireDate;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }
}
